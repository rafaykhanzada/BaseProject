        public ResultModel Get(int pageIndex = 0, int pageSize = int.MaxValue, string? Search = null)
        {
            try
            {
                _resultModel.Data = _mapper.Map<List<ModelDTO>>(_unitOfWork.ModelRepository.GetAllPaged(pageSize, pageIndex).ToList());
